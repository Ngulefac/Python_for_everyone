name = "Theodore"
age = int(19)
# python takes care of the data type
# in otherwords, you dont have to specify the data type during declaration
print("My name is",name,"and I am",age,"years old \n") 
# the '\n' simply stands for a new 
# you can also use the '+' when concatenating two strings
print("My name is "+name+" and I am",age,"years old \n") 