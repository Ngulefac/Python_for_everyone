# This program displays  a pattern
import turtle

turtle.color("black")
turtle.penup()
turtle.goto(-200, 200)
turtle.pendown()
turtle.write("FFFFFFF")
turtle.penup()
turtle.goto(-200, 180)
turtle.pendown()
turtle.write("FF")
turtle.penup()
turtle.goto(-200, 160)
turtle.pendown()
turtle.write("FFFFFFF")
turtle.penup()
turtle.goto(-200, 140)
turtle.pendown()
turtle.write("FF")
