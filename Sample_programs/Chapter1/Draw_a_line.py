# (Turtle: draw a line) This program  draws a red line connecting two points ( -39 , 48 ) and ( 50 , -50 )
# and displays the coordinates of the two points

import turtle
turtle.penup()
turtle.goto(-200, 100)
turtle.pendown()
turtle.write("( -39 , 48 )")
turtle.color("red")
turtle.right(45)
turtle.forward(300)

turtle.color("black")
turtle.write("( -39 , 48 )")


turtle.done()
